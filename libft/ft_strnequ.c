#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (n == 0)
		return (1);
	if (s1 && s2)
		return (ft_strncmp(s1, s2, n) != 0 ? 0 : 1);
	else
		return (0);
}
