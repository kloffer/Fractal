#include "libft.h"

size_t	ft_count_words(char const *s, char c)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	if (*(s + j) != '\0' && *(s + j) != c)
	{
		i++;
		j++;
	}
	while (*(s + j + 1) != '\0')
	{
		if (*(s + j) == c && *(s + j + 1) != c)
			i++;
		j++;
	}
	return (i);
}
